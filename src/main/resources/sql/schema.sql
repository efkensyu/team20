DROP TABLE IF EXISTS SHAIN_TBL;

CREATE TABLE SHAIN_TBL(
    SHAINCD VARCHAR2(20),
    SHAINNM VARCHAR2(20),
--   LOGINPASS VARCHAR2(20),
    RANK1 VARCHAR2(20),
    RANK2 VARCHAR2(20),
    RANK3 VARCHAR2(20),
    JOB VARCHAR2(20),
    INTRO VARCHAR2(200),

    PRIMARY KEY(SHAINCD)
);

DROP TABLE IF EXISTS LOGIN_TBL;
CREATE TABLE LOGIN_TBL(
    USERID VARCHAR(20),
    PASSWORD VARCHAR(20),
    PRIMARY KEY(USERID)
);

DROP TABLE IF EXISTS HOBBY_TBL;

CREATE TABLE HOBBY_TBL(
    HOBBYCODE VARCHAR2(20), 
    JANRU      VARCHAR2(50),
    HOBBY      VARCHAR2(50),
    PRIMARY KEY(HOBBYCODE)
);
