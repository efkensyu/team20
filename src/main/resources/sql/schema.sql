
CREATE TABLE SHAIN_TBL(
    SHAINCD VARCHAR2(20),
    SHAINNM VARCHAR2(20),
    LOGINPASS VARCHAR2(20),
    RANK1 VARCHAR2(20),
    RANK2 VARCHAR2(20),
    RANK3 VARCHAR2(20),
    JOB VARCHAR2(20),
    INTRO VARCHAR2(200),

    PRIMARY KEY(SHAINCD)
);


CREATE TABLE LOGIN_TBL(
    USERID VARCHAR(20),
    PASSWORD VARCHAR(20),
    PRIMARY KEY(USERID)
);
